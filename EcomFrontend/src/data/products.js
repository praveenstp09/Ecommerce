// ── Helper functions ──
export const getProductById = (id) => products.find(p => p.id === parseInt(id));

export const getProductsByCategory = (categoryId) =>
  products.filter(p => p.category === categoryId);

export const getFeaturedProducts = () =>
  products.filter(p => p.featured).slice(0, 8);

export const getTrendingProducts = () =>
  products.filter(p => p.trending).slice(0, 8);

export const getBestSellers = () =>
  [...products].sort((a, b) => b.reviewCount - a.reviewCount).slice(0, 8);

export const getNewArrivals = () =>
  products.filter(p => p.isNew).slice(0, 8);

export const getRelatedProducts = (productId, category) =>
  products.filter(p => p.category === category && p.id !== parseInt(productId)).slice(0, 4);

export const searchProducts = (query) => {
  const q = query.toLowerCase();
  return products.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.brand.toLowerCase().includes(q) ||
    p.category.toLowerCase().includes(q) ||
    p.tags.some(t => t.toLowerCase().includes(q))
  );
};
